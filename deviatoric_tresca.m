clf
clc
clear all;
Y = 1; %this is yield stress
r = sqrt(2/3) * Y;
c = cos(pi/6);
s = sin(pi/6);
% find 6 points of hexagonal in pi-plane in cartesian coordinate
X = [c,c,0,-c,-c,0,c]*r;
Y = [-s,s,1,s,-s,-1,-s]*r;
figure1= figure(1);
%plot tresca criteria
plot(X,Y,'LineWidth',2);
hold on;
% plot S3 direction is the same as y axes i.e. [0,1] direction
x = 0:0.0001:1.5;
plot(zeros(size(x)),x,'k','LineWidth',2);
% plot S1 direction along [sqrt(3)/2,-1/2] direction
plot(x*cos(-pi/6),x.*sin(-pi/6),'k','LineWidth',2);
% plot S2 direction along [-sqrt(3)/2,-1/2)] direction
plot(x*cos(-5*pi/6),x.*sin(-5*pi/6),'k','LineWidth',2);

hold off

grid on;
% Create textbox and put  S3 in desirable postion
annotation(figure1,'textbox',...
